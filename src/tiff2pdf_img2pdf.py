# ORIGINAL: Multi-page TIFF reader
# Iterates through TIFF IFD chain to read all pages
# ============================================================================
def read_tiff_file(tiff_path):
    """Read all pages from a TIFF file."""
    pages = []

    with open(tiff_path, 'rb') as f:
        # Read TIFF header (TIFF 6.0 spec)
        byte_order = f.read(2)
        endian = '<' if byte_order == b'II' else '>'
        magic = struct.unpack(endian + 'H', f.read(2))[0]
        if magic != 42:
            raise ValueError("Not a valid TIFF file")

        ifd_offset = struct.unpack(endian + 'I', f.read(4))[0]

        # Read all IFDs (following the IFD chain)
        while ifd_offset != 0:
            page, ifd_offset = read_tiff_ifd(f, ifd_offset, endian)
            if page:
                pages.append(page)

    return pages

