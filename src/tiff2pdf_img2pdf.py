# ORIGINAL: TIFF IFD (Image File Directory) Parser
# Based on TIFF 6.0 specification
# Reads TIFF tags and extracts image data for CCITT Group 4 compressed images
# ============================================================================
def read_tiff_ifd(f, offset, endian):
    """Read one TIFF IFD and extract image info."""
    # ORIGINAL: Error handling for EOF
    f.seek(offset)
    num_entries_data = f.read(2)
    if len(num_entries_data) < 2:
        return None, 0
    num_entries = struct.unpack(endian + 'H', num_entries_data)[0]

    # ORIGINAL: Initialize tag variables
    width = height = compression = fillorder = photo = None
    strip_offsets = strip_bytes = []

    # ORIGINAL: Parse each IFD entry (based on TIFF spec)
    for i in range(num_entries):
        entry_offset = offset + 2 + i * 12
        f.seek(entry_offset)
        tag, field_type, count = struct.unpack(endian + 'HHI', f.read(8))
        value_offset = f.tell()

        # ORIGINAL: Read value based on TIFF field type
        if field_type == 3:  # SHORT (16-bit)
            if count == 1:
                value = struct.unpack(endian + 'H', f.read(2))[0]
            else:
                if count * 2 <= 4:
                    value = [struct.unpack(endian + 'H', f.read(2))[0] for _ in range(count)]
                else:
                    ptr = struct.unpack(endian + 'I', f.read(4))[0]
                    pos = f.tell()
                    f.seek(ptr)
                    value = [struct.unpack(endian + 'H', f.read(2))[0] for _ in range(count)]
                    f.seek(pos)
        elif field_type == 4:  # LONG (32-bit)
            if count == 1:
                value = struct.unpack(endian + 'I', f.read(4))[0]
            else:
                ptr = struct.unpack(endian + 'I', f.read(4))[0]
                pos = f.tell()
                f.seek(ptr)
                value = [struct.unpack(endian + 'I', f.read(4))[0] for _ in range(count)]
                f.seek(pos)
        else:
            continue

        # ORIGINAL: Map TIFF tags to variables (tag numbers from TIFF spec)
        if tag == 256:  # ImageWidth
            width = value
        elif tag == 257:  # ImageLength
            height = value
        elif tag == 259:  # Compression
            compression = value
        elif tag == 262:  # PhotometricInterpretation
            photo = value
        elif tag == 266:  # FillOrder (concept from img2pdf: check this tag)
            fillorder = value
        elif tag == 273:  # StripOffsets
            strip_offsets = value if isinstance(value, list) else [value]
        elif tag == 279:  # StripByteCounts
            strip_bytes = value if isinstance(value, list) else [value]

    # Read next IFD offset for multi-page TIFFs
    f.seek(offset + 2 + num_entries * 12)
    next_ifd = struct.unpack(endian + 'I', f.read(4))[0]

    # Only process CCITT Group 4 (compression == 4)
    if compression == 4 and width and height and strip_offsets and strip_bytes:  # Group 4
        # ORIGINAL: Validate dimensions for safety
        if width <= 0 or height <= 0 or width > 20000 or height > 20000:
            print(f"Warning: Invalid dimensions {width}x{height}, skipping page")
            return None, next_ifd

        # Read raw compressed data from strips
        data = b''
        try:
            for off, size in zip(strip_offsets, strip_bytes):
                if size > 0:
                    f.seek(off)
                    chunk = f.read(size)
                    # ORIGINAL: Validate complete read
                    if len(chunk) != size:
                        print(f"Warning: Could not read complete strip data")
                        return None, next_ifd
                    data += chunk
        except Exception as e:
            print(f"Error reading strip data: {e}")
            return None, next_ifd

        if not data:
            print("Warning: No image data found")
            return None, next_ifd

        # Handle FillOrder (bit reversal for LSB2MSB)
        # Concept from img2pdf: check fillorder and reverse bits if needed
        # Uses TIFFBitRevTable from libtiff
        if fillorder == 2:  # LSB2MSB
            data = bytearray(data)
            for i in range(len(data)):
                data[i] = TIFFBitRevTable[data[i]]
            data = bytes(data)

        # Track photometric interpretation for BlackIs1 parameter
        # Concept from img2pdf: invert BlackIs1 when photometric == WhiteIsZero
        inverted = (photo == 0)  # WhiteIsZero
        return {'width': width, 'height': height, 'data': data, 'inverted': inverted}, next_ifd

    return None, next_ifd

