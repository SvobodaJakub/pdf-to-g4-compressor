# PDF/A-1B Creation
# IMPLEMENTATION: Original code, studied img2pdf's approach to CCITT embedding
# ORIGINAL: PDF/A metadata, CalGray colorspace, A4 scaling/centering
# ============================================================================
def create_pdf(pages, output_path):
    """Create PDF/A-1B compliant PDF from multiple CCITT images with A4 portrait pages."""
    # PDF header with binary marker
    pdf = b"%PDF-1.4\n%\xe2\xe3\xcf\xd3\n"

    # ORIGINAL: A4 portrait in points
    page_width_pt = 595
    page_height_pt = 842

    # Build objects array
    objects = []
    obj_num = 1

    # ORIGINAL: Reserve for PDF/A metadata
    metadata_obj_num = None
    output_intent_obj_num = None

    # ORIGINAL: CalGray color space (PDF/A compliant, device-independent)
    # Replaces DeviceGray to meet PDF/A requirements
    # D65 white point (standard for sRGB/daylight), Gamma 1.0 for neutral rendering
    calgray_colorspace = "[/CalGray << /WhitePoint [0.9505 1.0000 1.0890] /Gamma 1.0 >>]"

    # Reserve object number for Pages object (needed for /Parent references)
    # Each page needs 3 objects: image XObject, content stream, page object
    pages_obj_num = obj_num + (len(pages) * 3)

    # Create image and content objects for each page
    page_obj_nums = []
    for i, page in enumerate(pages):
        # ====================================================================
        # Image XObject
        # ====================================================================
        img_obj_num = obj_num
        obj_num += 1

        data_length = len(page['data'])

        # Image XObject dictionary
        # ColorSpace: ORIGINAL (CalGray instead of DeviceGray for PDF/A)
        # BlackIs1 logic: concept from img2pdf (inverted when photometric == 0)
        # DecodeParms format: concept from img2pdf (K=-1, Columns, Rows)
        # Decode array: ORIGINAL (explicit black/white mapping for neutral rendering)
        img_dict = (
            f"<< /Type /XObject "
            f"/Subtype /Image "
            f"/Width {page['width']} "
            f"/Height {page['height']} "
            f"/ColorSpace {calgray_colorspace} "  # ORIGINAL: CalGray for PDF/A
            f"/BitsPerComponent 1 "
            f"/Filter /CCITTFaxDecode "
            f"/DecodeParms << /K -1 /BlackIs1 {'false' if page['inverted'] else 'true'} "
            f"/Columns {page['width']} /Rows {page['height']} >> "
            f"/Decode [0 1] "  # ORIGINAL: Explicit sample 0→black (0.0), sample 1→white (1.0)
            f"/Length {data_length} >>"
        ).encode()

        # Embed raw CCITT data without re-encoding
        img_obj = img_dict + b"\nstream\n" + page['data'] + b"\nendstream"
        objects.append(img_obj)

        # ====================================================================
        # Content Stream - ORIGINAL: A4 scaling and centering logic
        # ====================================================================
        content_obj_num = obj_num
        obj_num += 1

        # ORIGINAL: Calculate aspect ratios and scale to fit A4
        img_aspect = page['width'] / page['height']
        page_aspect = page_width_pt / page_height_pt

        if img_aspect > page_aspect:
            # Image is wider - fit to width
            scale = page_width_pt / page['width']
        else:
            # Image is taller - fit to height
            scale = page_height_pt / page['height']

        scaled_width = page['width'] * scale
        scaled_height = page['height'] * scale

        # ORIGINAL: Center image on A4 page
        x_offset = (page_width_pt - scaled_width) / 2
        y_offset = (page_height_pt - scaled_height) / 2

        # Content stream with transformation matrix
        # ORIGINAL: Transformation matrix values for A4 centering
        content_stream = (
            f"q\n{scaled_width:.4f} 0 0 {scaled_height:.4f} {x_offset:.4f} {y_offset:.4f} cm\n/Im{i} Do\nQ\n"
        ).encode()

        content_obj = (
            f"<< /Length {len(content_stream)} >>".encode() +
            b"\nstream\n" + content_stream + b"\nendstream"
        )
        objects.append(content_obj)

        # ====================================================================
        # Page object
        # ORIGINAL: A4 MediaBox instead of image dimensions
        # ====================================================================
        page_obj_num = obj_num
        obj_num += 1
        page_obj_nums.append(page_obj_num)

        page_obj = (
            f"<< /Type /Page /Parent {pages_obj_num} 0 R "
            f"/MediaBox [0 0 {page_width_pt} {page_height_pt}] "  # ORIGINAL: Fixed A4 size
            f"/Resources << /XObject << /Im{i} {img_obj_num} 0 R >> >> "
            f"/Contents {content_obj_num} 0 R >>"
        ).encode()
        objects.append(page_obj)

    # ========================================================================
    # Pages object (object number was reserved earlier)
    # ========================================================================
    # pages_obj_num already set before the loop
    obj_num += 1

    kids = ' '.join([f"{n} 0 R" for n in page_obj_nums])
    pages_obj = (
        f"<< /Type /Pages /Kids [{kids}] /Count {len(pages)} >>"
    ).encode()
    objects.append(pages_obj)

    # ========================================================================
    # XMP Metadata stream - ORIGINAL: PDF/A-1B requirement
    # ========================================================================
    metadata_obj_num = obj_num
    obj_num += 1

    import datetime
    now = datetime.datetime.now(datetime.timezone.utc)
    timestamp = now.strftime('%Y-%m-%dT%H:%M:%S+00:00')

    # ORIGINAL: Full XMP metadata for PDF/A-1B compliance
    xmp_metadata = f'''<?xpacket begin="" id="W5M0MpCehiHzreSzNTczkc9d"?>
<x:xmpmeta xmlns:x="adobe:ns:meta/">
  <rdf:RDF xmlns:rdf="http://www.w3.org/1999/02/22-rdf-syntax-ns#">
    <rdf:Description rdf:about="" xmlns:dc="http://purl.org/dc/elements/1.1/">
      <dc:format>application/pdf</dc:format>
    </rdf:Description>
    <rdf:Description rdf:about="" xmlns:pdfaid="http://www.aiim.org/pdfa/ns/id/">
      <pdfaid:part>1</pdfaid:part>
      <pdfaid:conformance>B</pdfaid:conformance>
    </rdf:Description>
    <rdf:Description rdf:about="" xmlns:xmp="http://ns.adobe.com/xap/1.0/">
      <xmp:CreateDate>{timestamp}</xmp:CreateDate>
      <xmp:ModifyDate>{timestamp}</xmp:ModifyDate>
      <xmp:MetadataDate>{timestamp}</xmp:MetadataDate>
      <xmp:CreatorTool>tiff2pdf_img2pdf.py (PDF to CCITT Converter)</xmp:CreatorTool>
    </rdf:Description>
    <rdf:Description rdf:about="" xmlns:pdf="http://ns.adobe.com/pdf/1.3/">
      <pdf:Producer>tiff2pdf_img2pdf.py (PDF to CCITT Converter)</pdf:Producer>
    </rdf:Description>
  </rdf:RDF>
</x:xmpmeta>
<?xpacket end="w"?>'''.encode('utf-8')

    metadata_obj = (
        f"<< /Type /Metadata /Subtype /XML /Length {len(xmp_metadata)} >>".encode() +
        b"\nstream\n" + xmp_metadata + b"\nendstream"
    )
    objects.append(metadata_obj)

    # ========================================================================
    # OutputIntent - ORIGINAL: PDF/A-1B requirement
    # Declares the intended output condition (grayscale with gamma 2.2)
    # ========================================================================
    output_intent_obj_num = obj_num
    obj_num += 1

    output_intent_obj = (
        f"<< /Type /OutputIntent "
        f"/S /GTS_PDFA1 "
        f"/OutputConditionIdentifier (Gray Gamma 2.2) "
        f"/Info (Grayscale with Gamma 2.2) >>"
    ).encode()
    objects.append(output_intent_obj)

    # ========================================================================
    # Catalog
    # ORIGINAL: Added Metadata, OutputIntents, MarkInfo, ViewerPreferences for PDF/A
    # ========================================================================
    catalog_obj_num = obj_num
    obj_num += 1

    catalog_obj = (
        f"<< /Type /Catalog /Pages {pages_obj_num} 0 R "
        f"/Metadata {metadata_obj_num} 0 R "  # ORIGINAL: PDF/A requirement
        f"/OutputIntents [{output_intent_obj_num} 0 R] "  # ORIGINAL: PDF/A requirement
        f"/MarkInfo << /Marked true >> "  # ORIGINAL: PDF/A requirement
        f"/ViewerPreferences << /DisplayDocTitle true >> >>"  # ORIGINAL: PDF/A best practice
    ).encode()
    objects.append(catalog_obj)

    # ========================================================================
    # Write PDF structure
    # ========================================================================
    # Write objects and build xref table
    offsets = []
    for i, obj in enumerate(objects, start=1):
        offsets.append(len(pdf))
        pdf += f"{i} 0 obj\n".encode() + obj + b"\nendobj\n"

    # Write xref table
    xref_offset = len(pdf)
    pdf += b"xref\n"
    pdf += f"0 {len(objects) + 1}\n".encode()
    pdf += b"0000000000 65535 f \n"
    for offset in offsets:
        pdf += f"{offset:010d} 00000 n \n".encode()

    # ========================================================================
    # Trailer
    # ORIGINAL: Added ID array for PDF/A compliance
    # ========================================================================
    import hashlib
    import time

    # ORIGINAL: Generate unique file ID (PDF/A requirement)
    id_source = f"{output_path}{time.time()}{len(pages)}".encode()
    file_id = hashlib.md5(id_source).hexdigest().upper()

    pdf += b"trailer\n"
    pdf += (
        f"<< /Size {len(objects) + 1} "
        f"/Root {catalog_obj_num} 0 R "
        f"/ID [<{file_id}> <{file_id}>] >>\n"  # ORIGINAL ID array for PDF/A
    ).encode()
    pdf += b"startxref\n"
    pdf += f"{xref_offset}\n".encode()
    pdf += b"%%EOF\n"

    with open(output_path, 'wb') as f:
        f.write(pdf)

# ============================================================================
# ORIGINAL: Command-line interface
# Multi-file support for batch processing
# ============================================================================
if __name__ == '__main__':
    if len(sys.argv) < 3:
        print("Usage: tiff2pdf_img2pdf.py input1.tif [input2.tif ...] output.pdf")
        sys.exit(1)

    input_files = sys.argv[1:-1]
    output_file = sys.argv[-1]

    all_pages = []
    for tiff_path in input_files:
        if not os.path.exists(tiff_path):
            print(f"Error: {tiff_path} not found")
            sys.exit(1)

        try:
            pages = read_tiff_file(tiff_path)
            if not pages:
                print(f"Warning: No CCITT Group 4 pages in {tiff_path}")
            else:
                all_pages.extend(pages)
        except Exception as e:
            print(f"Error reading {tiff_path}: {e}")
            import traceback
            traceback.print_exc()
            sys.exit(1)

    if not all_pages:
        print("Error: No valid pages found")
        sys.exit(1)

    try:
        create_pdf(all_pages, output_file)
        print(f"Successfully created {output_file} with {len(all_pages)} page(s)")
    except Exception as e:
        print(f"Error creating PDF: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)
